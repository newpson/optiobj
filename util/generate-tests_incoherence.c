#include <stdio.h>
#include <stdint.h>

int main(void)
{
	// 00000000  00000000  00000000  00000000
	for (int32_t i = 64; i > 0; --i)
	{
		printf(
			"QTest::newRow(\"1%s%s 1%s%s 1%s%s\") << "
			"\"f 1/%s/%s 1/%s/%s 1/%s/%s\" << \n",

			((i >> 5) & 1) ? "1" : "0",
			((i >> 4) & 1) ? "1" : "0",
			((i >> 3) & 1) ? "1" : "0",
			((i >> 2) & 1) ? "1" : "0",
			((i >> 1) & 1) ? "1" : "0",
			((i >> 0) & 1) ? "1" : "0",

			((i >> 5) & 1) ? "1" : "",
			((i >> 4) & 1) ? "1" : "",
			((i >> 3) & 1) ? "1" : "",
			((i >> 2) & 1) ? "1" : "",
			((i >> 1) & 1) ? "1" : "",
			((i >> 0) & 1) ? "1" : "");
	}
}
